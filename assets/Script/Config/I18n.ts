export function normalizeLang(value?: string): LangCode {
    const normalized = String(value ?? '').trim().toLowerCase();
    if (!normalized) return DEFAULT_LANG;

    if (
        normalized === 'zh-hant'
        || normalized === 'zh-hk'
        || normalized === 'zh-tw'
        || normalized === 'zh-mo'
        || normalized.includes('hant')
        || normalized.includes('traditional')
    ) {
        return 'zh-Hant';
    }

    if (normalized === 'en' || normalized.startsWith('en-')) {
        return 'en';
    }

    if (normalized === 'vi' || normalized.startsWith('vi-') || normalized === 'vn' || normalized.startsWith('vn-')) {
        return 'vi';
    }

    if (normalized === 'ko' || normalized.startsWith('ko-') || normalized === 'kr' || normalized.startsWith('kr-')) {
        return 'ko';
    }

    if (normalized === 'ja' || normalized.startsWith('ja-') || normalized === 'jp' || normalized.startsWith('jp-')) {
        return 'ja';
    }

    return 'zh-Hans';
}

export class I18n {
    private static _lang: LangCode = DEFAULT_LANG;

    static init(lang?: string) {
        this._lang = normalizeLang(lang);
    }

    static get lang(): LangCode {
        return this._lang;
    }

    static t(key: string, params?: I18nParams) {
        const dict = DICTS[this._lang] ?? DICTS[DEFAULT_LANG];
        const template = dict[key] ?? key;
        if (!params) {
            return template;
        }

        return template.replace(/\{(\w+)\}/g, (_match, token: string) => {
            const value = params[token];
            return value != null ? String(value) : `{${token}}`;
        });
    }
}

export function t(key: string, params?: I18nParams) {
    return I18n.t(key, params);
}
